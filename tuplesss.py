def subsetsum(p):
    # ComplÃ©ter ce code. Complete this code here "
    num_list = list(p)
    head = num_list[0]
    num_list = reversed(sorted(num_list[1:]))

    sum = 0
    for num in num_list:
        if sum + num <= head:
            sum += num

    return sum


